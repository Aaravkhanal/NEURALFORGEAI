# ── Helpers ────────────────────────────────────────────────────

def _get_load_snippet(model_format: str, model_name: str) -> str:
    """Return the appropriate model loading code for a given format and model name."""
    
    # Framework specific override for generic formats like joblib/pkl
    if model_format in ["joblib", "pkl"]:
        name_lower = model_name.lower()
        if "xgboost" in name_lower:
            return "import joblib\nimport xgboost as xgb\n\ndef load_model(path):\n    # Framework: xgb.XGBClassifier() / xgb.XGBRegressor()\n    return joblib.load(path)"
        elif "catboost" in name_lower:
            return "import joblib\nimport catboost\n\ndef load_model(path):\n    # Framework: catboost.CatBoostClassifier() / catboost.CatBoostRegressor()\n    return joblib.load(path)"
        elif "lightgbm" in name_lower:
            return "import joblib\nimport lightgbm as lgb\n\ndef load_model(path):\n    # Framework: lgb.LGBMClassifier() / lgb.LGBMRegressor()\n    return joblib.load(path)"
        elif "neural network" in name_lower or "mlp" in name_lower:
            return "import joblib\nfrom sklearn.neural_network import MLPClassifier\n\ndef load_model(path):\n    # Framework: MLPClassifier()\n    return joblib.load(path)"
            
    snippets = {
        "joblib": "import joblib\n\ndef load_model(path):\n    return joblib.load(path)",
        "pkl": "import pickle\n\ndef load_model(path):\n    with open(path, 'rb') as f:\n        return pickle.load(f)",
        "onnx": "import onnxruntime as rt\nimport numpy as np\n\ndef load_model(path):\n    return rt.InferenceSession(path)",
        "json": "import xgboost as xgb\n\ndef load_model(path):\n    model = xgb.XGBClassifier()\n    model.load_model(path)\n    return model",
        "bst": "import xgboost as xgb\n\ndef load_model(path):\n    model = xgb.XGBClassifier()\n    model.load_model(path)\n    return model",
        "txt": "import lightgbm as lgb\n\ndef load_model(path):\n    return lgb.Booster(model_file=path)",
        "h5": "import tensorflow as tf\nfrom tensorflow import keras\n\ndef load_model(path):\n    # Framework: tf.keras.Sequential(...)\n    return keras.models.load_model(path)",
        "pt": "import torch\nimport torch.nn as nn\n\ndef load_model(path):\n    # Framework: torch.nn.Module\n    return torch.load(path, weights_only=False)",
        "pth": "import torch\nimport torch.nn as nn\n\ndef load_model(path):\n    # Framework: torch.nn.Module\n    return torch.load(path, weights_only=False)",
    }
    return snippets.get(model_format, "import joblib\n\ndef load_model(path):\n    return joblib.load(path)")
