class ProductionCodegenService:
    """Generates production-ready inference and deployment code."""

    @staticmethod
    def generate(
        target: str, model_format: str, model_name: str,
        task_type: str, feature_names: list = None, target_column: str = None,
        num_features: int = None,
    ) -> Dict[str, str]:
        """
        Generate production code for a given deployment target.
        Returns dict with 'code', 'filename', 'language' keys.
        """
        generators = {
            "python": ProductionCodegenService._gen_python,
            "fastapi": ProductionCodegenService._gen_fastapi,
            "flask": ProductionCodegenService._gen_flask,
            "streamlit": ProductionCodegenService._gen_streamlit,
            "docker": ProductionCodegenService._gen_docker,
            "aws": ProductionCodegenService._gen_aws_lambda,
            "gcp": ProductionCodegenService._gen_gcp_cloudrun,
            "azure": ProductionCodegenService._gen_azure_functions,
            "huggingface": ProductionCodegenService._gen_huggingface_spaces,
        }
        gen = generators.get(target.lower())
        if not gen:
            return {
                "code": f"# Code generation for target '{target}' is not supported yet.",
                "filename": "unsupported.py",
                "language": "python",
            }
        return gen(model_format, model_name, task_type, feature_names, target_column, num_features)

